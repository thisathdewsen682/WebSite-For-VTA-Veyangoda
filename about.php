                  <i class="fas fa-angle-down"></i>
               </div>
               <p class="accordion-content">
                  If the candidate could prove his/her ability with adequate evidence of his/her competencies pertaining
                  to qualifications stipulated in the Skills Standards, then the candidate is eligible to receive NVQ
                  certificate through RPL (Recognition of Prior Learning).

                  The other method of getting NVQ certificate is, by following a Competency Based Training (CBT) course
                  relevant to the qualification. A candidate can follow a NVQ based training program at Vocational
                  Training Authority or in other public and private institutes which are accredited to issue NVQ
                  certificates.
               </p>
            </div>

            <div class="accordion">
               <div class="accordion-heading">
                  <h3>Advantages of NVQ System<h3>
                        <i class="fas fa-angle-down"></i>
               </div>
               <p class="accordion-content">
                  NVQ Certificate is not a just a certificate to assure knowledge about the subject. It is a certificate
                  that certifies your skills. It is an internationally recognized certificate confirming that you
                  possess skills up to the respective NVQ level to work productively in the relevant profession.

                  Skills are automatically gained through the career growth, experiences and competencies which will
                  facilitate to obtain higher level of NVQ qualifications.

                  NVQ system is a skills evaluation system which has a higher tendency to obtain foreign employment.

                  Since the skills standards are prepared with the contribution and approval of professionally competent
                  personnel in the industry, such skills are sufficient to carry out the relevant functions in the
                  Industry. Therefore, it becomes easier for the NVQ certificate holders to enter the job market as
                  professionals.

                  If NVQ certificate recipients are enthusiastic to gain higher qualifications while working, the system
                  encourages employers to upgrade their career development
               </p>
